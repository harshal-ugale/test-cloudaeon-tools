export const generateAttendance = (employeeId: string): Attendance[] => {
  const records: Attendance[] = []
  const now = new Date('2025-05-08')
  const start = new Date('2025-04-01')
  const curr = new Date(start)

  while (curr <= now) {
    const day = curr.getDay()
    const dateStr = curr.toISOString().split('T')[0]

    if (day === 0 || day === 6) {
      records.push({ id: `att-${employeeId}-${dateStr}`, employeeId, date: dateStr, status: 'WEEKEND' })
    } else {
      const rand = Math.random()
      if (rand > 0.05) {
        records.push({
          id: `att-${employeeId}-${dateStr}`,
          employeeId,
          date: dateStr,
          checkIn: '09:' + String(Math.floor(Math.random() * 30)).padStart(2, '0'),
          checkOut: '18:' + String(Math.floor(Math.random() * 30)).padStart(2, '0'),
          status: 'PRESENT',
          hoursWorked: 8 + Math.random() * 2,
        })
      } else {
        records.push({ id: `att-${employeeId}-${dateStr}`, employeeId, date: dateStr, status: 'ABSENT' })
      }
    }

    curr.setDate(curr.getDate() + 1)
  }

  return records
}

